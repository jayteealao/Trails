package com.jayteealao.trails.ui.theme

import androidx.compose.material3.Typography
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.text.TextStyle
import androidx.compose.ui.text.font.FontFamily
import androidx.compose.ui.text.font.FontWeight
import androidx.compose.ui.text.googlefonts.Font
import androidx.compose.ui.text.googlefonts.GoogleFont
import androidx.compose.ui.unit.sp
import com.jayteealao.trails.R
import kotlinx.coroutines.CoroutineExceptionHandler
import timber.log.Timber

val fontProvider = GoogleFont.Provider(
    providerAuthority = "com.google.android.gms.fonts",
    providerPackage = "com.google.android.gms",
    certificates = R.array.com_google_android_gms_fonts_certs,
)

val piazzollaFont = FontFamily(
    Font(
        googleFont = GoogleFont("Piazzolla"),
        fontProvider = fontProvider,
        weight = FontWeight.SemiBold,
    )
)

val noricanFont = FontFamily(
    Font(
        googleFont = GoogleFont("Norican"),
        fontProvider = fontProvider,
        weight = FontWeight.W900,
    )
)

val fontHandler = CoroutineExceptionHandler { _, throwable ->
    Timber.e("CoroutineExceptionHandler got $throwable")
}

val Typography = Typography(
    bodyLarge = TextStyle(
        fontFamily = FontFamily.Default,
        fontWeight = FontWeight.Normal,
        fontSize = 16.sp,
        lineHeight = 24.sp,
        letterSpacing = 0.5.sp
    ),
    titleSmall = Typography().titleSmall.copy(
        color = Color.Gray
    ),
    titleMedium = Typography().titleMedium.copy(
        fontFamily = piazzollaFont,
    ),
    titleLarge = Typography().titleLarge.copy(
        fontFamily = noricanFont,
    ),
    /* Other default text styles to override
    titleLarge = TextStyle(
        fontFamily = FontFamily.Default,
        fontWeight = FontWeight.Normal,
        fontSize = 22.sp,
        lineHeight = 28.sp,
        letterSpacing = 0.sp
    ),
    labelSmall = TextStyle(
        fontFamily = FontFamily.Default,
        fontWeight = FontWeight.Medium,
        fontSize = 11.sp,
        lineHeight = 16.sp,
        letterSpacing = 0.5.sp
    )
    */
)
